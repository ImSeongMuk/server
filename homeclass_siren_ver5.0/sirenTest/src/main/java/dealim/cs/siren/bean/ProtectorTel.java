package dealim.cs.siren.bean;

public class ProtectorTel {
	private int userNum;
	private String protectCode;
	private String protectName;
	private String protectRelation;
	private String protectPhone;
	
	public String getProtectCode() {
		return protectCode;
	}
	public void setProtectCode(String protectCode) {
		this.protectCode = protectCode;
	}
	public int getUserNum() {
		return userNum;
	}
	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}
	public String getProtectName() {
		return protectName;
	}
	public void setProtectName(String protectName) {
		this.protectName = protectName;
	}
	public String getProtectRelation() {
		return protectRelation;
	}
	public void setProtectRelation(String protectRelation) {
		this.protectRelation = protectRelation;
	}
	public String getProtectPhone() {
		return protectPhone;
	}
	public void setProtectPhone(String protectPhone) {
		this.protectPhone = protectPhone;
	}	
}
